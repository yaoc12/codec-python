# Este programa determina si un número entero ingresado por el usuario es par o impar.

# Solicitar un número entero al usuario
numero = int(input("Ingrese un número entero: "))

# Verificar si el número es par (divisible por 2, resto 0)
if numero % 2 == 0:
    # Si es par, imprimir mensaje correspondiente
    print("El número es par.")
else:
    # Si no es par (es impar), imprimir mensaje correspondiente
    print("El número es impar.")

##Lógica condicional con if y else para determinar si un número entero ingresado por el usuario es par o impar. El resultado se muestra al final.